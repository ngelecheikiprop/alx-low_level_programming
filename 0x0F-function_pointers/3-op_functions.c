#include "3-calc.h"
/**
  *op_add -adds
  *@a: first number
  *@b: second number
  *
  *Return: addition
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  *op_sub -adds
  *@a: first number
  *@b: second number
  *
  *Return: addition
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  *op_mul -adds
  *@a: first number
  *@b: second number
  *
  *Return: addition
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div -adds
  *@a: first number
  *@b: second number
  *
  *Return: addition
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  *op_mod -adds
  *@a: first number
  *@b: second number
  *
  *Return: addition
  */
int op_mod(int a, int b)
{
	return (a % b);
}
